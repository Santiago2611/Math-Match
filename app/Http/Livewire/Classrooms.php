    public function addColumns(): PowerGridEloquent
    {
        return PowerGrid::eloquent()
            ->addColumn('id')
            ->addColumn('nombre_clase')
            ->addColumn('descripcion_clase')
            ->addColumn('tipo_clase')
            ->addColumn('vigente_hasta_formatted', fn (Classroom $model) => Carbon::parse($model->vigente_hasta)->format('d/m/Y'))
            ->addColumn('status')
            ->addColumn('grado')
            ->addColumn('teacher_id')
            ->addColumn('slug')
            ->addColumn('created_at_formatted', fn (Classroom $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'))
            ->addColumn('updated_at_formatted', fn (Classroom $model) => Carbon::parse($model->updated_at)->format('d/m/Y H:i:s'));
    }

